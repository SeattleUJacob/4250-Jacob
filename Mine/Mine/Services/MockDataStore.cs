using System;
using System.Collections.Generic;
using System.Linq;
using System.Threading.Tasks;
using Mine.Models;

namespace Mine.Services
{
    public class MockDataStore : IDataStore<ItemModel>
    {
        readonly List<ItemModel> items;

        public MockDataStore()
        {
            items = new List<ItemModel>()
            {
                new ItemModel { Id = Guid.NewGuid().ToString(), Text = "Smash ball", Description="Triggers your ultimate", Value=1 },
                new ItemModel { Id = Guid.NewGuid().ToString(), Text = "Pokeball", Description="Summons a pokemon", Value=2 },
                new ItemModel { Id = Guid.NewGuid().ToString(), Text = "Fishing pole", Description="Hook your enemies",Value=3 },
                new ItemModel { Id = Guid.NewGuid().ToString(), Text = "Rocket", Description="Launch at your foes", Value=4 },
                new ItemModel { Id = Guid.NewGuid().ToString(), Text = "Sappling", Description="Grows into a mighty oak", Value=5 }
            };
        }

        public async Task<bool> CreateAsync(ItemModel item)
        {
            items.Add(item);

            return await Task.FromResult(true);
        }

        public async Task<bool> UpdateItemAsync(ItemModel item)
        {
            var oldItem = items.Where((ItemModel arg) => arg.Id == item.Id).FirstOrDefault();
            items.Remove(oldItem);
            items.Add(item);

            return await Task.FromResult(true);
        }

        public async Task<bool> DeleteItemAsync(string id)
        {
            var oldItem = items.Where((ItemModel arg) => arg.Id == id).FirstOrDefault();
            items.Remove(oldItem);

            return await Task.FromResult(true);
        }

        public async Task<ItemModel> ReadAsync(string id)
        {
            return await Task.FromResult(items.FirstOrDefault(s => s.Id == id));
        }

        public async Task<IEnumerable<ItemModel>> GetItemsAsync(bool forceRefresh = false)
        {
            return await Task.FromResult(items);
        }
    }
}